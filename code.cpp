int read_state(){
  if (digitalRead(led_left) == HIGH) {
    return 0;
  }
  else if (digitalRead(led_right)==HIGH) {
    return 1;
  }
  else return 2;
}

void click(){
    if(!digitalRead(swl)){
      digitalWrite(led_left,HIGH);
      digitalWrite(led_right,LOW);

    }
    if (!digitalRead(swr)){
      digitalWrite(led_left,LOW);
      digitalWrite(led_right,HIGH);
    }
}

